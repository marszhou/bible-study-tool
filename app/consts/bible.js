import escapeStringRegexp from 'escape-string-regexp'

export const filterBooks = (filter, books) => {
  if (!filter) return books

  return books.filter(book => {
    const regex = new RegExp(escapeStringRegexp(filter), 'i')
    return (
      book.name.match(regex) ||
      book.nameEn.match(regex) ||
      book.namePy.match(regex) ||
      book.namePyInitial.match(regex)
    )
  })
}

export const getSelectorBookGroups = (lang = 'cn') => {
  return ['1', '2'].map(groupId => {
    const group = groupsById[groupId]
    return {
      id: +group.id,
      name: group.name_cn,
      books: group.books.map(bookId => {
        const book = booksById[bookId]
        return {
          id: +book.id,
          name: book.name_cn,
          nameAbbr: book.abbr_cn,
          nameEn: book.name_en,
          namePy: book.pinyin,
          namePyInitial: book.pinyin_initial,
          chapterCount: +book.chapter_count
        }
      })
    }
  })
}

export const getVerseCountOf = (bookId, chapter) => {
  const count = verseCountByBook[bookId] && verseCountByBook[bookId][chapter]
  if (count) {
    return +count
  }
  return null
}

export const getBook = bookId => booksById[bookId]

export const getPreviousChapter = ({ bookId, chapter }) => {
  const bookIndex = bookAllIds.indexOf(bookId+'')
  if (chapter === 1) {
    const previousBookId = bookAllIds[bookIndex - 1]
    if (previousBookId) {
      return {
        bookId: +previousBookId,
        chapter: +booksById[previousBookId].chapter_count
      }
    }
  } else {
    return {
      bookId,
      chapter: chapter - 1
    }
  }
}

export const getNextChapter = ({ bookId, chapter }) => {
  const bookIndex = bookAllIds.indexOf(bookId+'')
  const book = booksById[bookId]
  if (chapter === +book.chapter_count) {
    const nextBookId = bookAllIds[bookIndex + 1]
    if (nextBookId) {
      return {
        bookId: +nextBookId,
        chapter: 1
      }
    }
  } else {
    return {
      bookId,
      chapter: chapter + 1
    }
  }
}

